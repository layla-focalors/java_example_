import java.util.Scanner;

public class improved_for {
    public static void main(String[] args) {
        
        int[] arr = {1,2,3,4,5,6,7,8,9};
        // new for 문
        for(int e : arr){
            System.out.println(e);
            // 구성원의 값이 e로 들어가짐!
        }

        
        return;
    }
}

