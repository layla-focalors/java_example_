
import java.util.*;

public class operator02 {
    public static void main(String[] args) {
        // ------------------------------------------------------------------
        // 019_operator02 예제의 목표는 
        // 단항 연산자인 부호 연산자를 직접 실습.
        // ------------------------------------------------------------------

        int k = 10;
        System.out.println("+k = " + +k); // 부호를 양수로 변경
        System.out.println("-k = " + -k); // 부호를 음수로 변경

        System.out.println("k 출력 > " + k); // 원래 k값에는 변화가 없음
        return;
    }
}



